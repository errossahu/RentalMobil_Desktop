    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try
        {
            lamaPinjamException();
            inputException();
            pm = new PeminjamMahasiswa(txtNama.getText(),txtNoKtp.getText(),txtAlamat.getText(),Integer.parseInt(txtLamaPinjam.getText()),
            txtNamaKampus.getText(),txtAlamatKampus.getText(),0.0);
            pmc.editPeminjamMahasiswa(pm, txtNoKtp.getText());
//        pmc.editPeminjamMahasiswa(pm,i);
   
        
        int index = tblPeminjamMahasiswa.getRowCount();
        Double Total=0.0;
        for (int i=0; i<index; i++) {
            Double Biaya    = (Double) tblPeminjamMahasiswa.getValueAt(i, 6);
            Total=Total+Biaya;
        }
        txtTotalPendapatan.setText(Double.toString(Total));
        tampilDataTabel();
        dissField(false);
        clearText(pm);
        dissButton(false);

        }
        catch(InputException c)
        {
            JOptionPane.showMessageDialog(this, c.message());
        }
        catch(cobaException e)
        {
            JOptionPane.showMessageDialog(this, e.message());
        }
        
        
    }//GEN-LAST:event_btnSimpanActionPerformed

