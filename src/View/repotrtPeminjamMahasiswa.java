    private void tblPeminjamMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamMahasiswaMouseClicked
        // TODO add your handling code here:
        int index = tblPeminjamMahasiswa.getSelectedRow();
        if(index!=-1)
        { dissButton(true);
           
            String Nama        = (String) tblPeminjamMahasiswa.getValueAt(index, 0);
            String noKtp       = (String) tblPeminjamMahasiswa.getValueAt(index, 1);
            String Alamat      = (String) tblPeminjamMahasiswa.getValueAt(index, 2);
            int lamaPinjam     = (int) tblPeminjamMahasiswa.getValueAt(index, 3);
            String namaKampus  = (String) tblPeminjamMahasiswa.getValueAt(index, 4);
            String alamatKampus= (String) tblPeminjamMahasiswa.getValueAt(index, 5);
            double biaya       = (double) tblPeminjamMahasiswa.getValueAt(index, 6);
         
            txtNama.setText(Nama);
            txtNoKtp.setText(noKtp);
            txtAlamat.setText(Alamat);
            txtLamaPinjam.setText(String.valueOf(lamaPinjam));
            txtNamaKampus.setText(namaKampus);
            txtAlamatKampus.setText(alamatKampus);
            txtHarga.setText(String.valueOf(biaya));
 
        }
        else
        {
    
            dissButton(false);
        }
    
    }//GEN-LAST:event_tblPeminjamMahasiswaMouseClicked

