    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:

        try
        {
            inputException();
            lamaPinjamException();
            dataPeminjamPegawai();
            pm = new PeminjamPegawai(txtNama.getText(),txtNoKtp.getText(),txtAlamat.getText(),Integer.parseInt(txtLamaPinjam.getText()),
            txtNamaKantor.getText(),txtAlamatKantor.getText(),0.0);
            pmc.editPeminjamPegawai(pm, txtNoKtp.getText());
//        pmc.editPeminjamMahasiswa(pm,i);
   
        
            int index = tblPeminjamPegawai.getRowCount();
            Double Total=0.0;
            for (int i=0; i<index; i++) {
            Double Biaya    = (Double) tblPeminjamPegawai.getValueAt(i, 6);
            Total=Total+Biaya;
        }
            txtTotalPendapatan.setText(Double.toString(Total));
            tampilDataTabel();
            dissField(false);
            clearText(pm);
            dissButton(false);

        }catch(PeminjamExxception e)
            
        {
            JOptionPane.showMessageDialog(this, e.message());
            
            
        }
        catch(cobaException e)
        {
            JOptionPane.showMessageDialog(this, e.message());
        }
        catch(InputException e)
        {
            JOptionPane.showMessageDialog(this, e.message());
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

