    private void tblPeminjamPegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamPegawaiMouseClicked
        // TODO add your handling code here:
       int index = tblPeminjamPegawai.getSelectedRow();
        if(index!=-1)
        { dissButton(true);
           
            String Nama     = (String) tblPeminjamPegawai.getValueAt(index, 0);
            String noKtp    = (String) tblPeminjamPegawai.getValueAt(index, 1);
            String Alamat   = (String) tblPeminjamPegawai.getValueAt(index, 2);
            int lamaPinjam  = (int) tblPeminjamPegawai.getValueAt(index, 3);
            String namaKampus = (String) tblPeminjamPegawai.getValueAt(index, 4);
            String alamatKampus = (String) tblPeminjamPegawai.getValueAt(index, 5);
            double biaya    = (double) tblPeminjamPegawai.getValueAt(index, 6);
         
            txtNama.setText(Nama);
            txtNoKtp.setText(noKtp);
            txtAlamat.setText(Alamat);
            txtLamaPinjam.setText(String.valueOf(lamaPinjam));
            txtNamaKantor.setText(namaKampus);
            txtAlamatKantor.setText(alamatKampus);
            txtHarga.setText(String.valueOf(biaya));
 
        }
       else
        {
            dissButton(false);
        }
    }//GEN-LAST:event_tblPeminjamPegawaiMouseClicked

