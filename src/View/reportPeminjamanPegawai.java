    public void tampilDataTabel(){
      int a = tabelModel.getRowCount();
        for (int i = 0; i < a; i++) {
            tabelModel.removeRow(0);
        }

        
        List<PeminjamPegawai> PM = pmc.tampilPeminjam();
        for (int i = 0; i < PM.size(); i++) {
        pm = new PeminjamPegawai(PM.get(i).getNamaPeminjam(),PM.get(i).getNomorKtp(),PM.get(i).getAlamat(),PM.get(i).getLamaPeminjam(),PM.get(i).getNamaKantor(),PM.get(i).getAlamatKantor(),PM.get(i).getBiaya());
            addTablePegawai(pm);
        }
    
    }

