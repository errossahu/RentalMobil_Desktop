    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed

        if(namaPegawai.getText().equalsIgnoreCase(""))
        JOptionPane.showMessageDialog(this, "Inputkan nama !!");
        else
        {
            admin=AC.searchAdmin(namaPegawai.getText());
            if(admin==null)
            JOptionPane.showMessageDialog(this, "Nama Pegawai Tidak Ditemukan... !");
            else
            {
              
                txtNama.setText(admin.getNamaPegawai());
                txtNomor.setText(admin.getNomorPegawai());
                txtSandi.setText(admin.getKataSandi());
                setEnDisAdmin(true);
                action="edit";
                
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

