    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:

        if(namaPegawai.getText().equalsIgnoreCase(""))
            JOptionPane.showMessageDialog(this, "Tidak ada admin yang dipilih !");
        else
        {
   
            if(JOptionPane.showConfirmDialog(null, "Yakin Hapus?", "Yakin?",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_NO_OPTION)
            {
                AC.hapusAdmin(namaPegawai.getText());
                setEnDisAdmin(false);
                setText();
                namaPegawai.setText("");
                JOptionPane.showMessageDialog(this, "Data admin berhasil dihapus...");
            }
          
        }
    }//GEN-LAST:event_btnHapusActionPerformed

