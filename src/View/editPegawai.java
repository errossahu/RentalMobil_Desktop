    public void setEnDisAdmin(boolean val) {
        txtNama.setEnabled(val);
        txtNomor.setEnabled(val);
        txtSandi.setEnabled(val);
        btnHapus.setEnabled(val);
        btnSimpan.setEnabled(val);
        tblPegawai.setEnabled(false);
    }
    public void tampilPegawai()
    {
        int a = tabelModel.getRowCount();
        for (int i = 0; i < a; i++) {
            tabelModel.removeRow(0);
        }
        
        List<Pegawai> M = AC.tampilDataPegawai();
        for (int i = 0; i < M.size(); i++) {
            p = new Pegawai(M.get(i).getNamaPegawai(),M.get(i).getNomorPegawai(),M.get(i).getKataSandi());
            addTablePegawai(p);
        }
    }
